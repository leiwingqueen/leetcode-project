public class GetXORSum {
    /**
     * &å’Œ^ä¹Ÿæ»¡è¶³åˆ†é…å¾‹
     * <p>
     * (a&b)^(a&c)=a&(b^c)
     *
     *
     * @param arr1
     * @param arr2
     * @return
     */
    public int getXORSum(int[] arr1, int[] arr2) {
        int xor2 = 0;
        for (int i = 0; i < arr2.length; i++) {
            xor2 ^= arr2[i];
        }
        int res = 0;
        for (int i = 0; i < arr1.length; i++) {
            res ^= arr1[i] & xor2;
        }
        return res;
    }
}
