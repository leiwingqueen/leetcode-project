func maxValueAfterReverse(nums []int) int {
	n := len(nums)
	sum := 0
	for i := 0; i < n-1; i++ {
		sum += abs(nums[i] - nums[i+1])
	}
	res := sum
	for i := 0; i < n; i++ {
		for j := i; j < n; j++ {
			// å°è¯•ç¿»è½¬[i,j]ä¹‹é—´çš„åŒºé—´
			s := sum
			if i > 0 {
				s += abs(nums[i-1]-nums[j]) - abs(nums[i-1]-nums[i])
			}
			if j < n-1 {
				s += abs(nums[i]-nums[j+1]) - abs(nums[j]-nums[j+1])
			}
			if s > res {
				res = s
			}
		}
	}
	return res
}

func abs(num int) int {
	if num < 0 {
		return -num
	} else {
		return num
	}
}
